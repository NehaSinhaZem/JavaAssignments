package RodentsHierarchy;

class Hamster extends Rodent{
    Hamster(){
        System.out.println("I'm a Hamster");
    }
    void weight(){
        System.out.println("Weigh 110 to 140 g");
    }
    void habitat(){
        System.out.println("Terrestrial habitat, originated in the Middle East or southeastern Europe");
    }
    void behaviour(){
        System.out.println("Sniffing, Exploring, Burrowing, Squeaking, Grooming");
    }
    void communication(){
        System.out.println("Olfactory, Auditory, Visual");
    }
}
